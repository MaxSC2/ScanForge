export const JP_DICTIONARY = D;

const WORD_MAP = new Map<string, JpWordEntry[]>();

for (const entry of D) {
  const key = entry.word;
  if (!WORD_MAP.has(key)) WORD_MAP.set(key, []);
  WORD_MAP.get(key)!.push(entry);
  if (entry.reading && entry.reading !== entry.word) {
    if (!WORD_MAP.has(entry.reading)) WORD_MAP.set(entry.reading, []);
    WORD_MAP.get(entry.reading)!.push(entry);
  }
}

export function lookupJapaneseWord(word: string): JpWordEntry | undefined {
  return WORD_MAP.get(word)?.[0];
}

export function lookupJapaneseWordAll(word: string): JpWordEntry[] {
  return WORD_MAP.get(word) ?? [];
}
